myString = "0123456789"

print(myString[3:7]) # Starting value is included but ending is not
print(myString[:10]) # Very Start to 10-1=9
print(myString[0:]) # 0th term to the very end
print(myString[-8:-2]) # last term = -1 and first term = -n
print(myString[2:8]) # The above values are turned to positive and are swapped, this is same