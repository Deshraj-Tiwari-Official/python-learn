# union intersection of sets

set1 = {1, 2, 3, 4, 5}
set2 = {4, 5, 6, 7, 8}

# Union prodives a new set with all the elements of both sets but without any duplicates
print(set1.union(set2))

# Intersection on the other hand provides a new set with all the elements that are present in both
print(set1.intersection(set2))
